// demonstrating the use of command line arguments
public class ArrayDemo2
{
    public static void main( String[] args )
    {
        if ( args.length > 0 )
            // print the command line arguments
            for( int i = 0; i < args.length; i++ )
                System.out.print( args[ i ] + " " );
            System.out.println();
        
        
    }
}        
                
        
        
    